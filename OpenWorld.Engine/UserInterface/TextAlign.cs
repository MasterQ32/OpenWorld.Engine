using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace OpenWorld.Engine.UserInterface
{
	/// <summary>
	///	Specifies the text align of a control.
	/// </summary>
	public enum TextAlign
	{
		/// <summary>
		/// Text is aligned top left.
		/// </summary>
		TopLeft = 0,

		/// <summary>
		/// Text is aligned top center.
		/// </summary>
		TopCenter,

		/// <summary>
		/// Text is aligned top right.
		/// </summary>
		TopRight,

		/// <summary>
		/// Text is aligned middle left.
		/// </summary>
		MiddleLeft,

		/// <summary>
		/// Text is aligned middle center.
		/// </summary>
		MiddleCenter,

		/// <summary>
		/// Text is aligned middle right.
		/// </summary>
		MiddleRight,

		/// <summary>
		/// Text is aligned bottom left.
		/// </summary>
		BottomLeft,

		/// <summary>
		/// Text is aligned bottom center.
		/// </summary>
		BottomCenter,

		/// <summary>
		/// Text is aligned bottom right.
		/// </summary>
		BottomRight
	}
}
